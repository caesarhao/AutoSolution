using System;

namespace EasyOS
{
	public enum BaseType
	{
		BOOL = 0, // bool in C#
		UINT8 = 1, // byte in C#
		SINT8 = 2, // sbyte in C#
		UINT16 = 3, // ushort in C#
		SINT16 = 4, // short in C#
		UINT32 = 5, // uint in C#
		SINT32 = 6, // int in C#
		UINT64 = 7, // ulong in C#
		SINT64 = 8, // long in C#
		FLOAT32 = 9, // float in C#
		DOUBLE64 = 10 // double in C#
	}
}

